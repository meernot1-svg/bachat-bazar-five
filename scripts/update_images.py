# Track used images per category to avoid duplicates within nearby products
used_per_cat = {}

def get_unique_images(category, product_id, count=3):
    pool = CATEGORY_IMAGES.get(category, CATEGORY_IMAGES.get('home-lifestyle', []))
    if category not in used_per_cat:
        used_per_cat[category] = []

    result = []
    # Use product_id as offset to rotate through the pool
    start = (product_id * 3) % len(pool)

    for i in range(count):
        idx = (start + i) % len(pool)
        # Try to find an image not recently used
        attempts = 0
        while pool[idx] in used_per_cat[category][-6:] and attempts < len(pool):
            idx = (idx + 1) % len(pool)
            attempts += 1

        result.append(pool[idx])
        used_per_cat[category].append(pool[idx])

    return result

def update_data_file():
    with open(DATA_FILE, 'r') as f:
        content = f.read()

    lines = content.split('\n')
    new_lines = []
    changes = 0

    for line in lines:
        # Match: P(id,'name','brand','category',price,oldPrice,rating,reviews,stock,['imgs'],{...})
        m = re.match(
            r"^(\s+P\()(\d+),'([^']+)','([^']*)','([^']+)',(\d+),(\d+),([\d.]+),(\d+),(\d+),\[([^\]]+)\](,.*)$",
            line
        )
        if m:
            pid = int(m.group(2))
            category = m.group(5)
            old_imgs_str = m.group(11)
            prefix = m.group(1)
            name = m.group(3)
            brand = m.group(4)
            price = m.group(6)
            old_price = m.group(7)
            rating = m.group(8)
            reviews = m.group(9)
            stock = m.group(10)
            suffix = m.group(12)

            # Parse old images
            old_img_list = [img.strip().strip("'\"") for img in old_imgs_str.split(',')]

            # Check if any image is a markaz.app URL - keep it as first
            markaz_url = None
            for img in old_img_list:
                if 'static.markaz.app' in img or 'markaz' in img:
                    markaz_url = img
                    break

            # Get unique images for this product
            images = get_unique_images(category, pid, 3)

            if markaz_url:
                # Keep markaz URL as first, use 2 new unique images
                extra = get_unique_images(category, pid + 500, 2)
                images = [markaz_url] + extra

            img_str = ','.join(f"'{img}'" for img in images)
            new_line = f"{prefix}{pid},'{name}','{brand}','{category}',{price},{old_price},{rating},{reviews},{stock},[{img_str}]{suffix}"

            if new_line != line:
                changes += 1
            new_lines.append(new_line)
        else:
            new_lines.append(line)

    with open(DATA_FILE, 'w') as f:
        f.write('\n'.join(new_lines))

    print(f"Updated {changes} product image arrays")
    for cat, imgs in used_per_cat.items():
        print(f"  {cat}: {len(imgs)} images assigned")

if __name__ == '__main__':
    update_data_file()
